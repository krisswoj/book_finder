package pl.krzysiek.domain;

public class AllegroForApp {

    private String auctionName;
    private String auctionNumber;
    private String productPrice;
    private String lowestPriceDelivery;
    private String auctionImage;

    public String getAuctionName() {
        return auctionName;
    }

    public void setAuctionName(String auctionName) {
        this.auctionName = auctionName;
    }

    public String getAuctionNumber() {
        return auctionNumber;
    }

    public void setAuctionNumber(String auctionNumber) {
        this.auctionNumber = auctionNumber;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getLowestPriceDelivery() {
        return lowestPriceDelivery;
    }

    public void setLowestPriceDelivery(String lowestPriceDelivery) {
        this.lowestPriceDelivery = lowestPriceDelivery;
    }

    public String getAuctionImage() {
        return auctionImage;
    }

    public void setAuctionImage(String auctionImage) {
        this.auctionImage = auctionImage;
    }
}
